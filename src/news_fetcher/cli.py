# Configure logging
logger = logging.getLogger(__name__)


def setup_logging(verbose: bool = False):
    """
    Set up logging configuration.

    Args:
        verbose: Whether to enable verbose output
    """
    level = logging.DEBUG if verbose else logging.INFO
    logging.basicConfig(
        level=level,
        format="%(asctime)s - %(levelname)s - %(message)s",
        stream=sys.stderr
    )


# Main command group
@click.group(name="news-fetcher")
@click.option("--config", "-c", type=click.Path(exists=True), help="Configuration file path")
@click.option("--sources", "-s", help="Comma-separated list of source URLs (override config)")
@click.option("--since", type=click.DateTime(), help="Only fetch articles published after this time")
@click.option("--limit", "-l", type=int, default=50, help="Maximum number of articles to return (default: 50)")
@click.option("--diversity", "-d", type=float, default=0.6, help="MMR lambda parameter 0-1 (default: 0.6)")
@click.option("--min-score", type=float, default=0.3, help="Minimum score threshold (default: 0.3)")
@click.option("--output", "-o", type=click.Path(), help="Output file path")
@click.option("--format", "-f", type=click.Choice(["json", "markdown", "csv"]), default="json", help="Output format (default: json)")
@click.option("--fixtures", type=click.Path(), help="Use local fixture files instead of fetching")
@click.option("--verbose", "-v", is_flag=True, help="Verbose output")
@click.pass_context
def main(ctx, config, sources, since, limit, diversity, min_score, output, format, fixtures, verbose):
    """News Fetcher - A news aggregation and clustering tool."""
    setup_logging(verbose)
    ctx.ensure_object(dict)

    # Store common parameters in context
    ctx.obj["config_path"] = config
    ctx.obj["sources"] = sources
    ctx.obj["since"] = since
    ctx.obj["limit"] = limit
    ctx.obj["diversity"] = diversity
    ctx.obj["min_score"] = min_score
    ctx.obj["output"] = output
    ctx.obj["format"] = format
    ctx.obj["fixtures"] = fixtures
    ctx.obj["verbose"] = verbose

    try:
        # Initialize configuration
        if config:
            ctx.obj["config"] = get_config(config)
    except Exception as e:
        logger.error(f"Configuration error: {e}")
        sys.exit(1)


