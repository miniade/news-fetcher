# Main pipeline execution (default when no subcommand)
@main.command()
@click.pass_context
def run(ctx):
    """Run the news fetching pipeline (default command)."""
    try:
        if ctx.obj["fixtures"]:
            click.echo("Running pipeline from fixtures...")
            pipeline = create_default_pipeline(ctx.obj["config_path"])
            result = pipeline.run_from_fixtures([ctx.obj["fixtures"]])
        else:
            click.echo("Running news pipeline...")
            pipeline = create_default_pipeline(ctx.obj["config_path"])
            result = pipeline.run(limit=ctx.obj["limit"])

        # Output results
        if ctx.obj["output"]:
            # TODO: Implement output writing
            click.echo(f"Writing output to {ctx.obj['output']}")
        else:
            # Print summary to console
            click.echo(f"\nResults:")
            click.echo(f"Total articles: {len(result.articles)}")
            click.echo(f"Total clusters: {len(result.clusters)}")
            click.echo("\nArticles:")
            for i, article in enumerate(result.articles[:ctx.obj['limit']], 1):
                click.echo(f"{i}. {article.title}")
                click.echo(f"   Source: {article.source}")
                click.echo(f"   Published: {article.published_at}")
                click.echo(f"   Score: {article.score:.2f}")
                if article.summary:
                    click.echo(f"   Summary: {article.summary}")
                click.echo()

    except NewsFetcherError as e:
        logger.error(f"Error: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        if ctx.obj["verbose"]:
            import traceback
            logger.error(traceback.format_exc())
        sys.exit(1)


if __name__ == "__main__":
    main()
